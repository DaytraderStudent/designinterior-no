// ─── 1. Add to products.ts ───────────────────────────────────────
const productsPath = join(__dirname, "..", "src", "data", "products.ts");
let content = readFileSync(productsPath, "utf-8");

// Find the closing bracket of the array
const lastBracket = content.lastIndexOf("]");

// Build new product entries
const entries = newProducts.map((p) => {
  return `  {
    id: "${p.id}",
    name: "${p.name}",
    brand: "${p.brand}",
    category: "${p.category}",
    price: ${p.price},
    image_url: "${p.image_url}",
    affiliate_url: "#",
    style_tags: ${JSON.stringify(p.style_tags)},
    color_tags: ${JSON.stringify(p.color_tags)},
    room_tags: ${JSON.stringify(p.room_tags)},
    dimensions: { width: ${p.dimensions.width}, depth: ${p.dimensions.depth}, height: ${p.dimensions.height} },
    in_stock: true,
  }`;
}).join(",\n");

const newContent = content.slice(0, lastBracket) + entries + ",\n];\n";
writeFileSync(productsPath, newContent, "utf-8");
console.log(`products.ts: Added ${newProducts.length} products`);

// ─── 2. Add to seed.mjs ─────────────────────────────────────────
const seedPath = join(__dirname, "seed.mjs");
let seedContent = readFileSync(seedPath, "utf-8");

const seedInsertLine = seedContent.lastIndexOf("]);");
if (seedInsertLine > 0) {
  const seedEntries = newProducts.map((p) => {
    return `    { name: "${p.name}", brand: "${p.brand}", category: "${p.category}", price: ${p.price}, image_url: "${p.image_url}", affiliate_url: "#", style_tags: ${JSON.stringify(p.style_tags)}, color_tags: ${JSON.stringify(p.color_tags)}, room_tags: ${JSON.stringify(p.room_tags)}, dimensions: { width: ${p.dimensions.width}, depth: ${p.dimensions.depth}, height: ${p.dimensions.height} }, in_stock: true }`;
  }).join(",\n");

  seedContent = seedContent.slice(0, seedInsertLine) + seedEntries + ",\n  ]);\n" + seedContent.slice(seedInsertLine + 3);
  writeFileSync(seedPath, seedContent, "utf-8");
  console.log(`seed.mjs: Added ${newProducts.length} products`);
}

// ─── 3. Insert into Supabase ─────────────────────────────────────
let inserted = 0;
let errors = 0;

for (const p of newProducts) {
  const { error } = await supabase.from("products").insert({
    name: p.name,
    brand: p.brand,
    category: p.category,
    price: p.price,
    image_url: p.image_url,
    affiliate_url: "#",
    style_tags: p.style_tags,
    color_tags: p.color_tags,
    room_tags: p.room_tags,
    dimensions: p.dimensions,
    in_stock: true,
  });

  if (error) {
    errors++;
    if (errors <= 3) console.error(`  Error: ${p.brand} ${p.name}: ${error.message}`);
  } else {
    inserted++;
  }
}

console.log(`\nSupabase: Inserted ${inserted} products (${errors} errors)`);

